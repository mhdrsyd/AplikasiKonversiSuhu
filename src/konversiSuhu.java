    private boolean autoConvert = false;
    private String satuanAktif = "째C";


    private void konversiSuhu() {

         try {
        // 2. Ambil nilai suhu dan satuan awal
        // Hapus simbol derajat agar bisa dibaca sebagai angka
        String inputText = txtsuhu.getText().replaceAll("[^0-9.-]", "");
        double suhuAwal = Double.parseDouble(inputText);

        String satuanAwal = cbmSatuan.getSelectedItem().toString();
        String satuan = "";

        // 3. Tentukan satuan tujuan berdasarkan radio button
        String satuanTujuan = "";
        if (r1Fahrenheit.isSelected()) {
            satuanTujuan = "Fahrenheit";
            satuan = "째F";
        } else if (r2Celcius.isSelected()) {
            satuanTujuan = "Celcius";
            satuan = "째C";
        } else if (r3Kelvin.isSelected()) {
            satuanTujuan = "Kelvin";
            satuan = "째K";
        } else if (r4Reamur.isSelected()) {
            satuanTujuan = "Reamur";
            satuan = "째R";
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih satuan suhu tujuan terlebih dahulu!");
            return;
        }

        // 4. Proses konversi
        double hasil = 0;
        switch (satuanAwal) {
            case "Celcius(째C)" -> {
                switch (satuanTujuan) {
                    case "Fahrenheit" -> hasil = (suhuAwal * 9 / 5) + 32;
                    case "Reamur" -> hasil = suhuAwal * 4 / 5;
                    case "Kelvin" -> hasil = suhuAwal + 273.15;
                    case "Celcius" -> hasil = suhuAwal;
                }
            }
            case "Reamur(째R)" -> {
                switch (satuanTujuan) {
                    case "Celcius" -> hasil = suhuAwal * 5 / 4;
                    case "Fahrenheit" -> hasil = (suhuAwal * 9 / 4) + 32;
                    case "Kelvin" -> hasil = (suhuAwal * 5 / 4) + 273.15;
                    case "Reamur" -> hasil = suhuAwal;
                }
            }
            case "Fahrenheit(째H)" -> {
                switch (satuanTujuan) {
                    case "Celcius" -> hasil = (suhuAwal - 32) * 5 / 9;
                    case "Reamur" -> hasil = (suhuAwal - 32) * 4 / 9;
                    case "Kelvin" -> hasil = (suhuAwal + 459.67) * 5 / 9;
                    case "Fahrenheit" -> hasil = suhuAwal;
                }
            }
            case "Kelvin(째K)" -> {
                switch (satuanTujuan) {
                    case "Celcius" -> hasil = suhuAwal - 273.15;
                    case "Reamur" -> hasil = (suhuAwal - 273.15) * 4 / 5;
                    case "Fahrenheit" -> hasil = (suhuAwal - 273.15) * 9 / 5 + 32;
                    case "Kelvin" -> hasil = suhuAwal;
                }
            }
        }

        // 5. Tampilkan hasil di TextField
        txtHasil.setText(String.format("%.2f 째%s", hasil, satuanTujuan.charAt(0) ,satuan));

    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Input harus berupa angka yang valid!");
    }
    }
    
    private void updateInputText() {
    String teks = txtsuhu.getText();

    // Hapus simbol lama (jika ada)
    teks = teks.replaceAll("\\s*째[A-Z]", "").trim();

    // Tambahkan satuan baru
    if (!teks.isEmpty()) {
        txtsuhu.setText(teks + " " + satuanAktif);
    }
}

    
    private void cbmSatuanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbmSatuanActionPerformed
        // TODO add your handling code here:
    String pilihan = cbmSatuan.getSelectedItem().toString();
    
    // Tentukan simbol derajat sesuai pilihan
    switch (pilihan) {
        case "Celcius(째C)" -> satuanAktif = "째C";
        case "Reamur(째R)" -> satuanAktif = "째R";
        case "Fahrenheit(째H)" -> satuanAktif = "째F";
        case "Kelvin(째K)" -> satuanAktif = "째K";
    }

    // Perbarui tampilan input agar ada derajat
    updateInputText();

    // Jika mode auto aktif, konversi juga langsung dijalankan
    if (autoConvert) {
        konversiSuhu();
    }
    }//GEN-LAST:event_cbmSatuanActionPerformed

